package assignment_8;

public class Q_5 {

	public static void main(String[] args) {

		sign(-3);

	}

	public static void sign(int num) {
		if (num > 0) {
			System.out.println("1");

		} else if (num < 0) {
			System.out.println("-1");

		} else {
			System.out.println("0");
		}
	}

}